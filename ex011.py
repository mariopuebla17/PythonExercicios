l = float(input('Informe a largura da parede, em metros: '))
a = float(input('Informe a altura da parece, em metros: '))
area = l * a
tinta = area / 2
print('A área dessa parede é de {} metros quadrados.'.format(area))
print('Para isso, será necessário {} litros de tinta para a pintura.'.format(tinta))
